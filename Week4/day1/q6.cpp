#include <iostream>
#include <vector>
using namespace std;

int main() {
    int n;
    cout << "Enter number of elements: ";
    cin >> n;

    vector<int> a(n);
    cout << "Enter the elements:\n";
    for(int i=0;i<n;i++) cin >> a[i];

    for(int i=0;i<n-1;i++) {
        int minIndex = i;
        for(int j=i+1;j<n;j++)
            if(a[j] < a[minIndex])
                minIndex = j;
        int t = a[i];
        a[i] = a[minIndex];
        a[minIndex] = t;
    }

    cout << "Sorted array: ";
    for(int i=0;i<n;i++) cout << a[i] << " ";
    return 0;
}
